<script lang="ts">
import { defineComponent, ref, computed, onMounted } from 'vue';

export default defineComponent({
	name: 'JackpotCounter',
	setup() {
		const jackpotAmount = ref(8294547501);

		// Format the amount with commas
		const formattedAmount = computed(() => {
			return jackpotAmount.value.toLocaleString();
		});

		// Simulate jackpot increment for demo purposes
		onMounted(() => {
			// Real-time jackpot updates every 3 seconds
			const interval = setInterval(() => {
				// Add random increment between 1000 and 5000
				const increment = Math.floor(Math.random() * 4000) + 1000;
				jackpotAmount.value += increment;
			}, 1500);

			// Cleanup interval on component unmount
			return () => {
				clearInterval(interval);
			};
		});

		return {
			formattedAmount
		};
	}
});
</script>

